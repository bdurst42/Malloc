#include "string.h"

size_t	ft_nbrlen(long long nb, int base)
{
	int	i;

	i = 0;
	if (nb < 0)
		i++;
	if (nb == 0)
		return (1);
	while (nb)
	{
		i++;
		nb /= base;
	}
	return (i);
}
