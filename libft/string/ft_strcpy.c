#include "string.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int lg;

	lg = 0;
	while (src[lg] != '\0')
	{
		dst[lg] = src[lg];
		lg++;
	}
	dst[lg] = '\0';
	return (dst);
}
